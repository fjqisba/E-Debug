//到十六进制
void CopyHexData(Script::Gui::Window window)
{
	duint start = 0;
	duint end = 0;
	if (!Script::Gui::SelectionGet(window, &start, &end)) {
		return;
	}
	
	unsigned int dataSize = end - start + 1;

	std::vector<unsigned char> hexData;
	hexData.resize(dataSize, 0x0);
	if (!Script::Memory::Read(start, &hexData[0], dataSize, 0)) {
		return;
	}

	std::string strHex;
	strHex.resize(dataSize << 1);
	unsigned int index = 0;
	for (unsigned int n = 0; n < hexData.size(); ++n) {
		const char* pHex = UCharToStr(hexData[n]);
		strHex[index++] = pHex[0];
		strHex[index++] = pHex[1];
	}

	QClipboard* pClipboradr = QApplication::clipboard();
	if (pClipboradr) {
		pClipboradr->setText(QString::fromStdString(strHex.c_str()));
	}
}

//到字节集
void CopyBinData(Script::Gui::Window window)
{
	duint start = 0;
	duint end = 0;
	if (!Script::Gui::SelectionGet(window, &start, &end)) {
		return;
	}

	unsigned int dataSize = end - start + 1;
	if (!dataSize) {
		return;
	}

	std::vector<unsigned char> hexData;
	hexData.resize(dataSize, 0x0);
	if (!Script::Memory::Read(start, &hexData[0], dataSize, 0)) {
		return;
	}

	QString strBin;
	strBin.reserve(hexData.size() * 4 + 1);
	strBin.append("{");
	for (unsigned int n = 0; n < hexData.size(); ++n) {
		strBin.append(QString::number(hexData[n]));
		strBin.append(",");
	}
	strBin.chop(1);
	strBin.append("}");

	QClipboard* pClipboradr = QApplication::clipboard();
	if (pClipboradr) {
		pClipboradr->setText(strBin);
	}
}

void EDebugEntry()
{
	if (!DbgIsDebugging()) {
		Script::Gui::Message("Please Load a Program!");
		return;
	}

	duint startAddr = 0;
	duint endAddr = 0;
	if (!SelectionGet(Script::Gui::DisassemblyWindow, &startAddr, &endAddr)) {
		Script::Gui::Message("Unexpected error!");
		return;
	}

	//debug版和release版本是不同的Qt环境
#ifdef _DEBUG
	int argc = 0;
	QApplication app(argc, 0);
	MainWindow* pWindow = new MainWindow(startAddr);
	pWindow->show();
	app.exec();
	return;
#else
	MainWindow* pWindow = new MainWindow(startAddr);
	QDesktopWidget* desktop = QApplication::desktop();
	pWindow->move((desktop->width() - pWindow->width()) / 2, (desktop->height() - pWindow->height()) / 2);
	pWindow->show();
#endif
	return;
}

